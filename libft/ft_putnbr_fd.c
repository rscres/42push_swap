#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	x;

	x = 0;
	if (n == -2147483648)
	{
		write(fd, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n / 10)
	{
		ft_putnbr_fd((n / 10), fd);
	}
	x = n % 10 + '0';
	write (fd, &x, 1);
}
